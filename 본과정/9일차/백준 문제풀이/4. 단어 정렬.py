# 입력
# 첫째 줄에 단어의 개수 N
# 둘째 줄부터 N개의 줄에 걸쳐 알파벳 소문자로 이루어진 단어가 한 줄에 하나씩 주어진다.

# 출력
# 조건에 따라 정렬하여 단어들을 출력한다.
# 1. 길이가 짧은 것부터
# 2. 길이가 같으면 사전 순으로
# 단, 중복된 단어는 하나만 남기고 제거해야 한다.

# 로직
# 1. 단어의 개수 반복문 
# 2. 13개 단어 리스트에 담아줌 
# 3. set 사용하여 중복 제거 
# 4. 길이가 짧은 순으로 -> 단어 len 기준으로
# 5. 길이가 같으면 사전 순으로

# 문법 미숙으로 인해 GPT 활용하여 해결

n = int(input())

words = [list(input().split()) for _ in range(n)] # 입력받은 단어 리스트로 반환
setWords = list(map(list, set(map(tuple, words)))) # 중복 제거
# 리스트를 튜블로 변환 -> why? set 함수는 변경 불가능한 자료형만 취급하는데 
# 리스트는 변경 가능한 자료형이므로 tuple로 바꿔준뒤 중복을 제거하고
# 다시 리스트로 반환

setWords.sort(key=lambda x: (len(x[0]), x[0]))
# key 인자에 튜플을 사용하여 정렬 기준을 설정
# 첫 번째 기준은 **단어의 길이(len(x[0]))**로, 짧은 순서대로 정렬
# 두 번째 기준은 **사전 순 정렬(x[0])**로, 길이가 같은 단어는 사전 순으로 정렬

for word in setWords:
  print(''.join(word))